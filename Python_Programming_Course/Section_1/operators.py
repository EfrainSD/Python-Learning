# Arithmetic Operators
print(3 + 4)   # Addition
print(3 - 4)   # Subtraction
print(3 * 4)   # Multiplication
print(3 / 4)   # Division
print(3 // 4)  # Floor Division
print(3 % 4)   # Modulus
print(3 ** 4)  # Exponentiation

# Boolean Operators
print(3 > 4)    # Greater than
print(3 < 4)    # Less than
print(3 >= 4)   # Greater than or equal to
print(3 <= 4)   # Less than or equal to
print(3 == 4)   # Equal to
print(3 != 4)   # Not equal to

# Logical Operators
print(True and False)  # Logical AND
print(True or False)   # Logical OR
print(not True)        # Logical NOT